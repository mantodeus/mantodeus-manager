function DropdownMenuContent({
  className,
  sideOffset = 4,
  ...props
}: React.ComponentProps<typeof DropdownMenuPrimitive.Content>) {
  const menuRef = React.useRef<HTMLDivElement>(null);
  const [isOpen, setIsOpen] = React.useState(false);

  // Watch for open state changes via data-state attribute
  // Also watch for when element is added to DOM (for portal-rendered menus)
  React.useEffect(() => {
    const checkState = () => {
      if (menuRef.current) {
        const state = menuRef.current.getAttribute('data-state');
        setIsOpen(state === 'open');
      }
    };

    if (!menuRef.current) {
      // If ref not ready, check again after a short delay
      const timeout = setTimeout(() => {
        if (menuRef.current) {
          checkState();
        }
      }, 0);
      return () => clearTimeout(timeout);
    }

    const observer = new MutationObserver((mutations) => {
      mutations.forEach((mutation) => {
        if (mutation.type === 'attributes' && mutation.attributeName === 'data-state') {
          checkState();
        }
        // Also watch for when element is added to DOM
        if (mutation.type === 'childList') {
          checkState();
        }
      });
    });

    observer.observe(menuRef.current, {
      attributes: true,
      attributeFilter: ['data-state'],
      childList: false,
      subtree: false,
    });

    // Also observe the document body for portal-rendered menus
    const bodyObserver = new MutationObserver(() => {
      if (menuRef.current) {
        checkState();
      }
    });

    bodyObserver.observe(document.body, {
      childList: true,
      subtree: false,
    });

    // Initial check
    checkState();

    // Also check periodically as a fallback (less frequent to avoid performance issues)
    const pollInterval = setInterval(checkState, 100);

    return () => {
      observer.disconnect();
      bodyObserver.disconnect();
      clearInterval(pollInterval);
    };
  }, []);

  // Enable auto-scroll on all devices to prevent dropdowns from being cropped
  // Use larger buffer on mobile to account for tab bar
  const isMobile = typeof window !== 'undefined' && window.innerWidth < 768;
  useAutoScrollOnOpen({
    isOpen,
    menuRef,
    enabled: true,
    scrollBuffer: isMobile ? 24 : 12, // Larger buffer on mobile for tab bar clearance
  });

  return (
    <DropdownMenuPrimitive.Portal>
      <DropdownMenuPrimitive.Content
        ref={menuRef}
        data-slot="dropdown-menu-content"
        sideOffset={sideOffset}
        className={cn(
          "glass-context-menu text-popover-foreground z-50 max-h-(--radix-dropdown-menu-content-available-height) origin-(--radix-dropdown-menu-content-transform-origin) overflow-x-hidden overflow-y-auto",
          className
        )}
        {...props}
      />
    </DropdownMenuPrimitive.Portal>
  );
}

